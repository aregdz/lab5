if __name__ == '__main__':
    n = int(input("Введите чилсо времени года:  "))
    if n == 1:
        print("Зима: декабрь, январь, февраль")
    elif n == 2:
        print("Весна: март, апрель, май")
    elif n == 3:
        print("Лето: июнь, июль, август")
    else:
        print("Осень: сентябрь, октябрь, ноябрь")